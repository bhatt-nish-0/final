package decision.making;

public class WithElse {
    public static void main(String[] args) {
        int y = 20;

        if (y == 20) {
            System.out.println("1");
        }else {
            System.out.println("2");
        }

        if (y==20)
            System.out.println("who");
        else
            System.out.println("hey");
    }
}
